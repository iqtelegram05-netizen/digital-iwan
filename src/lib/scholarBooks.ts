/**
 * الحصول على قائمة الكتب الخاصة بالمرجع المختار
 * مع الكتب المشتركة الأساسية
 */
export function getScholarBooks(scholarName: string): ScholarData | null {
  return SCHOLAR_BOOKS[scholarName] || null;
}

/**
 * بناء تذييل الـ prompt الخاص بالمرجع
 * يُقيّد الإجابة على كتب هذا المرجع فقط
 */
export function buildScholarPromptSuffix(scholarName: string): string {
  const scholar = SCHOLAR_BOOKS[scholarName];
  
  if (!scholar) {
    // مرجع مخصص من قبل المستخدم (غير موجود في القائمة)
    return `

⚠️ قاعدة حاسمة إضافية: يجب أن تجيب وفق منهج وفتاوى "${scholarName}" حصرياً.
- استند إلى فتاوى "${scholarName}" المتوفرة لديك.
- إذا لم تكن تعرف فتوى "${scholarName}" في مسألة معينة، قل: "لم أتمكن من العثور على فتوى صريحة لسماحة ${scholarName} في هذه المسألة. يرجى مراجعة رسالته العملية أو الاستفتاء مباشرة من مكتبه."
- يُحظر عليك أن تنسب فتوى لـ "${scholarName}" إذا لم تكن متأكداً منها.
- المصادر المساندة المسموح بها: القرآن الكريم، أحاديث أهل البيت (ع)، الكتب الأربعة (الكافي، من لا يحضره الفقيه، التهذيب، الاستبصار)، الوسائل للحر العاملي، الجواهر للجواهري.`;
  }

  const booksList = scholar.books.map(b => `- ${b}`).join('\n');
  const suppList = scholar.supplementaryBooks.map(b => `- ${b}`).join('\n');

  return `

════════════════════════════════════════════════════
⚠️ قاعدة حاسمة إضافية: المرجع المُختار حالياً هو ${scholar.name} (${scholar.title}).
════════════════════════════════════════════════════

يجب أن تلتزم بالقواعد التالية بدقة:

1. مصادرك الأساسية هي كتب ${scholar.name} فقط:
${booksList}

2. الكتب المساندة المسموح بها (للتوثيق فقط):
${suppList}

3. إذا سُئلت عن حكم فقهي أو مسألة شرعية:
   - ابدأ بفتوى ${scholar.name} الصريحة من كتبه المذكورة أعلاه
   - اذكر اسم الكتاب والمجلد والصفحة إن أمكن
   - لا تذكر آراء مراجع آخرين إلا إذا كان ${scholar.name} نفسه يشير إليها
   - إذا لم تعرف رأي ${scholar.name} في مسألة معينة، قل بوضوح: "لم أتمكن من العثور على فتوى صريحة لسماحة ${scholar.name} في هذه المسألة المحددة. يرجى مراجعة كتاب [اسم أقرب كتاب للمرجع] أو الرجوع لمكتب ${scholar.name} مباشرة."

4. يُحظر عليك نسبة أي فتوى أو رأي فقهي إلى ${scholar.name} إذا لم تكن متأكداً منها.

5. عند ذكر الأدلة، ابدأ بفتوى المرجع ثم استشهد بالكتب المساندة إن لزم الأمر.`;
}

/**
 * بناء prompt للأستاذ الرقمي (teacher) مقيد بالمرجع
 */
export function buildTeacherScholarPrompt(scholarName: string): string {
  const scholar = SCHOLAR_BOOKS[scholarName];
  
  if (!scholar) {
    return `

⚠️ أنت الآن تُدرّس وفق منهج "${scholarName}" حصرياً.
- اشرح المسائل وفق فتاوى "${scholarName}" وكتبه.
- إذا لم تعرف رأيه في مسألة، قل ذلك بوضوح ولا تُفترض.
- المصادر المساندة: القرآن الكريم، أحاديث أهل البيت، الكتب الأربعة، الوسائل.`;
  }

  return `

════════════════════════════════════════════════════
📚 التدريس الآن مقيد بمنهج: ${scholar.name}
════════════════════════════════════════════════════

كأستاذ حوزوي، يجب أن:
1. تُدرّس المسائل الفقهية وفق فتاوى ${scholar.name} من كتبه:
${scholar.books.map(b => `   - ${b}`).join('\n')}
2. تُوضّح منهج ${scholar.name} في الاستنباط والاجتهاد
3. تقارن بين رأي ${scholar.name} وآراء علماء الشيعة الآخرين إن لزم الأمر، مع الترجيح لرأي المرجع المختار
4. إذا لم يكن لـ ${scholar.name} رأي صريح في مسألة، قل: "لم أجد رأياً صريحاً لسماحة ${scholar.name} في هذه المسألة."
5. الكتب المساندة المسموح بها:
${scholar.supplementaryBooks.map(b => `   - ${b}`).join('\n')}`;
}
