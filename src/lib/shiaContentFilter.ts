export function addShiaDisclaimer(text: string, detected: boolean): string {
  if (detected) {
    return text + '\n\n⚠️ تنبيه: بعض المصادر المذكورة أعلاه ليست من المراجع الشيعية الإمامية. يرجى الاعتماد على الكتب الشيعية المعتمدة فقط.';
  }
  return text;
}

/**
 * فلترة متقدمة - تجمع بين جميع الطرق
 * هذه هي الوظيفة الرئيسية التي ينبغي استخدامها
 */
export function advancedShiaFilter(text: string): string {
  if (!text || text.length < 10) return text;

  // الخطوة 1: الفحص السريع
  const detected = hasNonShiaReferences(text);

  if (!detected) {
    return text; // النظيف - لا يحتاج معالجة
  }

  // الخطوة 2: التنظيف العميق (حذف الجمل الملوثة)
  let cleaned = deepCleanShiaContent(text);

  // الخطوة 3: الفلترة المتبقية (استبدال الكلمات)
  cleaned = filterShiaContent(cleaned);

  // الخطوة 4: التحقق النهائي
  if (hasNonShiaReferences(cleaned)) {
    // تنظيف إضافي
    cleaned = filterShiaContent(cleaned);
  }

  return cleaned;
}
