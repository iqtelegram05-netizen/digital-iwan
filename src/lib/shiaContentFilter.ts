/**
 * فلترة المحتوى - الوظيفة الرئيسية
 * تفحص النص وتزيل/تستبدل أي مرجع لمصادر غير شيعية
 */
export function filterShiaContent(text: string): string {
  if (!text || text.length < 10) return text;

  let filtered = text;

  // 1. تطبيق البدائل الشيعية (أولوية عالية)
  for (const repl of SHIA_REPLACEMENTS) {
    filtered = filtered.replace(repl.pattern, repl.replacement);
  }

  // 2. كشف المراجع لكتب محظورة - إزالة الجملة التي تحتوي على الكتاب المحظور
  for (const book of BLOCKED_BOOKS) {
    for (const pattern of book.patterns) {
      filtered = filtered.replace(pattern, '[كتاب محظور - ليس من مصادرنا الشيعية]');
    }
  }

  // 3. كشف المراجع لعلماء محظورين كمصدر
  for (const scholar of BLOCKED_SCHOLARS) {
    for (const pattern of scholar.patterns) {
      filtered = filtered.replace(pattern, '[عالم غير شيعي - ليس من مراجعنا]');
    }
  }

  // 4. الكلمات المفتاحية المشبوهة
  for (const pattern of SUSPICIOUS_PATTERNS) {
    filtered = filtered.replace(pattern, '[مصدر غير شيعي]');
  }

  return filtered;
}

/**
 * فحص سريع - هل النص يحتوي على مراجع غير شيعية؟
 * يُستخدم لاتخاذ قرار بإعادة توليد الإجابة
 */
export function hasNonShiaReferences(text: string): boolean {
  if (!text || text.length < 10) return false;

  // فحص سريع بأهم الكلمات المفتاحية
  const quickCheckPatterns = [
    /البخاري/,
    /صحيح مسلم/,
    /سنن الترمذي/,
    /سنن أبي داود/,
    /سنن النسائي/,
    /ابن ماجه/,
    /الموطأ/,
    /مسند أحمد/,
    /أبو حنيفة/,
    /مالك بن أنس/,
    /الشافعي/,
    /أحمد بن حنبل/,
    /ابن تيمية/,
    /الألباني/,
    /ابن كثير/,
    /القرطبي/,
    /الطبري/,
    /النووي/,
    /ابن حجر/,
    /الذهبي/,
    /الغزالي/,
    /رواه البخاري/,
    /رواه مسلم/,
    /أخرجه البخاري/,
    /أخرجه مسلم/,
    /في صحيح البخاري/,
    /في صحيح مسلم/,
    /عند الحنفية/,
    /عند المالكية/,
    /عند الشافعية/,
    /عند الحنابلة/,
    /المذاهب الأربعة/,
    /متفق عليه/,
  ];

  for (const pattern of quickCheckPatterns) {
    if (pattern.test(text)) return true;
  }

  return false;
}

/**
 * فحص وتنظيف شامل - يحذف الجمل التي تحتوي على مراجع غير شيعية بالكامل
 * بدلاً من مجرد استبدال الكلمة
 */
export function deepCleanShiaContent(text: string): string {
  if (!text || text.length < 10) return text;

  const lines = text.split('\n');
  const cleanedLines: string[] = [];

  for (const line of lines) {
    if (hasNonShiaReferences(line)) {
      // تخطي السطر الذي يحتوي على مرجع غير شيعي بالكامل
      // لكن نضيف تنبيهاً بدلاً منه
      continue;
    }
    cleanedLines.push(line);
  }

  let result = cleanedLines.join('\n');

  // تنظيف فراغات زائدة
  result = result.replace(/\n{3,}/g, '\n\n').trim();

  return result;
}

